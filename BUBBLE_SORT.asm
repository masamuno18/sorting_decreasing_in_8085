;<<BUBBLE SORTING/COMPARISON SORTING ALGORITHM>>
;STORING DATA IN MEMORY [05 32 16 48 64 80]>
MVI A,	06H
STA 0000H
MVI A,	05H ; 05
STA 0001H
MVI A,	20H ; 32
STA 0002H
MVI A,	10H ; 16
STA 0003H
MVI A,	30H ; 48
STA 0004H
MVI A,	40H ; 64
STA 0005H
MVI A,	50H ; 80
STA 0006H
	
;CODE FOR SORTING IN DECREASING ORDER
START:	LXI H,0000H ; GIVES THE NUMBER OF DATA IN ARRAY 
	MVI D,00H
	MOV C,M
	DCR C
	INX H ;POINTING TO NEXT LOCATION
CHECK:	MOV A,M 
	INX H
	CMP M
	JNC NXTBYT ; JUMP IF C=0
	MOV B,M
	MOV M,A
	DCX H
	MOV M,B
	INX H
	MVI D,01H
NXTBYT:	DCR C
	JNZ CHECK; IF COMPARISON COUNT NOT 0 RERUN THE LOOP
	MOV A,D
	RRC 
	JC START
	HLT